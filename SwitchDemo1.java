class SwitchDemo1 
{
	public static void main(String[] args) 
	{
	   int name =4;
	   String gName = "name";
	   switch(name)
		{
		   case 1: 
			   gName="ADi";
		   break;
		   case 2:
			   gName="ANI";
		   break;
		   case 3:
			   gName="AJ";
		   break;
		   case 4:
			   gName="RJ";
		   break;
		}
		 System.out.println(gName);
	   
	}
}
